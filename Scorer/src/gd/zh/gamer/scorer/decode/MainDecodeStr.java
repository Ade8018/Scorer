	boolean codecrc16(char[] code_orig_buf) {
		char crchi = 0x00;
		char crclo = 0x00;
		int i;
		int len = code_orig_buf.length - crc_len;
		char tmp = 0;

		while (len-- != 0) {
			i = crclo ^ code_orig_buf[tmp++];
			crclo = (char) (crchi ^ crc_table_hi[i]);
			crchi = crc_table_lo[i];
		}

		if (crchi == code_orig_buf[tmp] && crclo == code_orig_buf[tmp + 1]) {
			return true;
		}
		return false;
	}

	char playqr_decode_fun0(char code) {
		int c = code ^ 0x5a;
		c = ~c;
		return (char) (c & 0x000000ff);
	}

	char playqr_decode_fun1(char code) {
		int c = code - 5;
		c = c & 0x000000ff;
		c = ((c >> 4) | (c << 4));
		return (char) (c & 0x000000ff);
	}

	char playqr_decode_fun2(char code) {
		int c = ((code >> 4) | (code) << 4);
		c = c - 5;
		return (char) (c & 0x000000ff);
	}

	char playqr_decode_fun3(char code) {
		int c = ((code << 2) & 0x000000fc);
		c = c | ((code >> 6) & 0x00000003);
		return (char) (c & 0x000000ff);
	}

	char playqr_decode_fun4(char code) {
		int c = ((code >> 6) & 0x00000003);
		c = (c | ((code << 6) & 0x000000c0));
		c = (c | ((code >> 2) & 0x0000000c));
		c = (c | ((code << 2) & 0x00000030));
		return (char) (c & 0x000000ff);
	}

	char playqr_decode_fun5(char code) {
		int c = ~code;
		c = c - 0xdd;
		return (char) (c & 0x000000ff);
	}

	char playqr_decode_fun6(char code) {
		int c = code ^ 0x5a;
		c = c + 0x55;
		return (char) (c & 0x000000ff);
	}

	char playqr_decode_fun7(char code) {
		int c = code + 0x5a;
		c = c ^ 0x55;
		return (char) (c & 0x000000ff);
	}

	char playqr_decode_fun8(char code) {
		int c = ~code;
		return (char) (c & 0x000000ff);
	}

	char playqr_decode_fun9(char code) {
		int c = code ^ 0xc3;
		c = (c >> 4) | (c << 4);
		return (char) (c & 0x000000ff);
	}

	public String decodeStr(String str) {
		int dec_len = code_orig_len;
		char c_orig[] = str.substring(head_len, head_len + code_orig_len).toCharArray();
		char c_dec[] = str.substring(head_len + code_orig_len, head_len + code_orig_len * 2).toCharArray();
		char c_neg[] = str.substring(head_len + code_orig_len * 2).toCharArray();
		int neg_bit = 0;

		for (int i = 0; i < c_neg.length; i++) {
			for (int j = 0; j < 6; j++) {
				if ((i * 6 + j) >= code_orig_len)
					break;
				if (((c_neg[i] >> j) & 0x0001) == 1) {
					c_orig[neg_bit + j] |= 0x80;
				}
			}
			neg_bit += 6;
		}

		for (int i = 0; i < dec_len; i++) {
			if ((c_dec[i] % 10) == 0)
				c_dec[i] = playqr_decode_fun0(c_orig[i]);
			else if ((c_dec[i] % 10) == 1)
				c_dec[i] = playqr_decode_fun1(c_orig[i]);
			else if ((c_dec[i] % 10) == 2)
				c_dec[i] = playqr_decode_fun2(c_orig[i]);
			else if ((c_dec[i] % 10) == 3)
				c_dec[i] = playqr_decode_fun3(c_orig[i]);
			else if ((c_dec[i] % 10) == 4)
				c_dec[i] = playqr_decode_fun4(c_orig[i]);
			else if ((c_dec[i] % 10) == 5)
				c_dec[i] = playqr_decode_fun5(c_orig[i]);
			else if ((c_dec[i] % 10) == 6)
				c_dec[i] = playqr_decode_fun6(c_orig[i]);
			else if ((c_dec[i] % 10) == 7)
				c_dec[i] = playqr_decode_fun7(c_orig[i]);
			else if ((c_dec[i] % 10) == 8)
				c_dec[i] = playqr_decode_fun8(c_orig[i]);
			else if ((c_dec[i] % 10) == 9)
				c_dec[i] = playqr_decode_fun9(c_orig[i]);
		}

		if (codecrc16(c_dec) == false) {
			this.human_str = "校验错误，二维码无效";
			return this.human_str;
		}

		this.dec_str = String.copyValueOf(c_dec);
		this.human_str = "打印机SN号:  " + this.dec_str.substring(0, sn_len) + "\r\n" + "打印日期:  " + this.dec_str.substring(sn_len, sn_len + date_len) + "\r\n" + "游戏分数:  "
				+ this.dec_str.substring(sn_len + date_len, sn_len + date_len + num_len) + "\r\n";

		return this.human_str;
	}

	public boolean isPpCode(String str) {
		char head1 = str.charAt(0);
		char head2 = str.charAt(1);

		if ((head1 != code_head1) || (head2 != code_head2)) {
			return false;
		}
		return true;
	}

